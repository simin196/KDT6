# 10번 숫자 데이터를 입력을 받습니다.
# - 숫자 데이터를 모두 더해서 합계가 30이상이 되면
#  10번 입력 안 받았더라도 종료해주세요.
#-------------------------------------------------------

total=0

for n in range(1,11):
    d =int( input(f'{n}째 숫자를 넣어주세요 : '))
    total = total+d
    if total >= 30: break

